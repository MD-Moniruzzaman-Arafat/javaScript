var n = 90;

switch (n) {
    case 90:
        console.log("A+ grade");
        break;
    case 80:
        console.log("A grade");
        break;
    case 70:
        console.log("A- grade");
        break;
    case 60:
        console.log("B grade");
        break;
    case 50:
        console.log("C grade");
        break;
    default:
        console.log("F grade");
        break;
}